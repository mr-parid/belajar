<?php
//PEMBELAJARAN REPEAT

//Dibawah Ini Contoh For
for ($i=0; $i <5; $i++) {
    echo "Parid Dudin <br>";
}

//Dibawah Ini Contoh While
$i=0;
while ($i<=5) {
    echo "Parid Dudin <br>";
    $i++;
}

//Dibawah Ini Contoh Do While
$i=0;
do {
    echo "Parid Dudin <br>";
    $i++;
} while ($i<=7);
?>